    private boolean verify() {
          if(!txtUsername.getText().equals("")&&!txtPassword.getText().equals("")){
            return true;
        }
        return false;
    }

    private boolean login() {
          String data[] = service.findByUsername(txtUsername.getText());
        if(data!=null){
        if (txtPassword.getText().equals(data[1])){
                return true;
            }
        }
        return false;
        
    }
    
     private void clear(){
        txtUsername.setText("");
        txtPassword.setText("");
    }
