    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        if (!txtCode.getText().equals("")) {
            String[] data = departmentService.find(txtCode.getText());
            if (data != null) {
                txtName.setText(data[1]);
                //this.cmbManagemen.setSelectedItem(data[2]);
                this.cmbManagemen.setSelectedItem(allMcmb(data[2]));
                txtLocation.setText(data[3]);
                txtPhone.setText(data[4]);
                txtEmail.setText(data[5]);
                txtCode.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(this, "Codigo de departamento no encontrado", "ERROR", 2);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe digitar un valor", "ERROR", 2);
        }
    }//GEN-LAST:event_btnFindActionPerformed

