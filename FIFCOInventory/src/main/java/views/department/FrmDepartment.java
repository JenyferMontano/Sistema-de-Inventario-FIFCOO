    private boolean verifyAll() {
        if (!txtCode.getText().equals("") && !txtName.getText().equals("") && !txtLocation.getText().equals("") && !txtPhone.getText().equals("") && !txtEmail.getText().equals("")) {
            return true;
        }
        return false;
    }

    private void clear() {
        txtCode.setEditable(true);
        txtCode.setText("");
        txtName.setText("");
        this.cmbManagemen.setSelectedItem("");
        txtLocation.setText("");
        txtPhone.setText("");
        txtEmail.setText("");
        clearTable();
    }

    private void allManagemensN() {
        String[][] allManagemens = managemenService.list();
        for (String[] allMan : allManagemens) {
            listManagemens.add(allMan[1] + " " + allMan[2] + " (" + allMan[0] + ")");
        }

    }

    private String allManagemensC(String fullM) {
        String data = "";
        String[][] allManagemens = managemenService.list();
        for (String[] allMan : allManagemens) {
            data = allMan[1] + " " + allMan[2] + " (" + allMan[0] + ")";
            if (data.equals(fullM)) {
                return allMan[0];
            }
        }
        return null;
    }

    private String allMcmb(String code) {
        String data = "";
        String[][] allManagemens = managemenService.list();
        for (String[] allMan : allManagemens) {
            data = allMan[1] + " " + allMan[2] + " (" + allMan[0] + ")";
            if (allMan[0].equals(code)) {
                return data;
            }
        }
        return null;
    }

    private boolean getAll() {
        String data[][] = departmentService.list();
        if (data != null) {
            for (String[] managemen : data) {
                dtm.addRow(managemen);
            }
            return true;
        }

        return false;
    }

    private void clearTable() {
        dtm.setRowCount(0);
        btnGetAll.setEnabled(true);
    }
